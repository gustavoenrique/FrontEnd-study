  
const a = 7
let b = 3

b += a // b = b + a
console.log(b)

b -= 4 // b = b - 4
console.log(b)

b *= 2 // b = b * 2
console.log(b)

b /= 2 // b = b / 2
console.log(b)

b %= 5 // b = b % 2 // atribuição modular ou seja, se o resto da divisão
                    // for par ele retorna 0, se for ímpar retorna 1
console.log(b)